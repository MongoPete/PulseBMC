"use client";
import { useCallback, useEffect, useRef, useState } from "react";
import ConceptBar from "@/components/ConceptBar";
import QueryTooltip from "@/components/QueryTooltip";
import RootCauseCard from "@/components/RootCauseCard";
import WorkOrderCard from "@/components/WorkOrderCard";
import RetrievedContextPanel from "@/components/RetrievedContextPanel";
import LedIndicator from "@/components/LedIndicator";
import GuidedTour, { TourCard, WhyBox, Pill } from "@/components/GuidedTour";
import { api, SSE_URL } from "@/lib/api";
import { fmtDateTime, fmtRelative } from "@/lib/time";
import type { Step } from "react-joyride";

interface Alert {
  id: string;
  device_id: string;
  summary: string;
  severity: string;
  status: string;
  failure_rate: number;
  triggered_at: string;
}

interface RootCause {
  alert_id: string;
  root_cause_hypothesis: string;
  evidence: string[];
  confidence: number;
  alternative_hypotheses: string[];
  next_diagnostic_steps: string[];
  retrieved_context_summary: string;
}

interface WorkOrder {
  title: string;
  priority: "P1" | "P2" | "P3" | "P4";
  assigned_technician: string;
  repair_steps: string[];
  estimated_duration_minutes: number;
  required_parts: string[];
  safety_notes: string[];
  historical_basis: string;
  originating_alert_id: string;
}

interface RetrievedDoc {
  collection: string;
  doc_id: string;
  similarity?: number;
  summary: string;
}

interface ChainResult {
  prediction: Record<string, unknown>;
  root_cause: RootCause;
  work_order: WorkOrder;
  agent_run_id: string;
  cached?: boolean;
  cached_at?: string;
}

interface KnowledgeBase {
  total_runs: number;
  latest_run_at: string | null;
  top_hypotheses: Array<{ id: string; count: number; avg_confidence: number; devices: string[]; last_seen: string }>;
  at_risk_components: Array<{ id: string; appearances: number; avg_failure_rate: number; error_codes: string[] }>;
  priority_distribution: Array<{ priority: string; count: number }>;
}

const SEVERITY_COLORS: Record<string, string> = {
  critical: "border-rose-800/60 bg-rose-950/10",
  high:     "border-amber-800/50 bg-amber-950/10",
  medium:   "border-yellow-800/40 bg-yellow-950/10",
  low:      "border-slate-700/60 bg-slate-900/20",
};

